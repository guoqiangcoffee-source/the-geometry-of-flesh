def main():
    print(f"\n全书 16 章结尾批量重写 — {ROOT}")
    print("=" * 70)

    total_fixed = 0
    total_skipped = 0
    total_warn = 0

    for filename, anchor, new_ending in FIXES:
        path = BOOK / filename
        if not path.exists():
            print(f"  [missing] {filename}")
            total_warn += 1
            continue

        text = path.read_text(encoding="utf-8")

        # 找到锚点位置
        idx = text.find(anchor)
        if idx == -1:
            # 检查是否已经替换过(如果新内容已存在,跳过)
            new_first_line = new_ending.strip().split("\n")[0]
            if new_first_line in text:
                print(f"  [skip]    §{filename[:2]} 已是修订版")
                total_skipped += 1
            else:
                print(f"  [WARN]    §{filename[:2]} 未找到锚点: '{anchor[:30]}...'")
                total_warn += 1
            continue

        # 替换:从锚点开始到章末,全部替换为新内容
        new_text = text[:idx] + new_ending
        # 保留章末可能有的额外标记(如果有的话)——但当前所有章节末尾都没有 *(本章未完)* 等标记,直接替换即可
        path.write_text(new_text, encoding="utf-8")
        print(f"  [fixed]   §{filename[:2]} {filename}")
        total_fixed += 1

    print("=" * 70)
    print(f"完成: 修复 {total_fixed} 处, 已修订 {total_skipped} 处, 未找到 {total_warn} 处")

